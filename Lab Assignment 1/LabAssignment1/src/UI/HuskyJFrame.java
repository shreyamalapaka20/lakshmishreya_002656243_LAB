    private void display() {
        viewNUID.setText(String.valueOf(this.person.getNuid()));
        viewFirstName.setText(this.person.getFirstName());
        viewLastName.setText(this.person.getLastName());
        viewClgName.setText(this.person.getClgName());
        
        viewOffPhone.setText(String.valueOf(this.person.getoContact().getPhone()));
        viewOffEmail.setText(this.person.getoContact().getEmail());
        
        viewPersPhone.setText(String.valueOf(this.person.getpContact().getPhone()));
        viewPersEmail.setText(this.person.getpContact().getEmail());
        
        
        viewCurrStreet.setText(this.person.getCurrAddress().getStreet());
        viewCurrApt.setText(this.person.getCurrAddress().getApt());
        viewCurrCity.setText(this.person.getCurrAddress().getCity());
        viewCurrZip.setText(String.valueOf(this.person.getCurrAddress().getZip()));
        
        viewPermStreet.setText(this.person.getPermAddress().getStreet());
        viewPermApt.setText(this.person.getPermAddress().getApt());
        viewPermCity.setText(this.person.getPermAddress().getCity());
        viewPermZip.setText(String.valueOf(this.person.getPermAddress().getZip()));  
    }
    
    
