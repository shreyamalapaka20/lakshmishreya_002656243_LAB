    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        
        long nuid = Long.valueOf(fieldNUID.getText());
        String firstName = fieldFirstName.getText();
        String lastName = fieldLastName.getText();
        String clgName = fieldClgName.getText();
        
        long oPhone = Long.valueOf(fieldOffPhone.getText());
        String oEmail = fieldOffEmail.getText();
        
        long pPhone = Long.valueOf(fieldPersPhone.getText());
        String pEmail = fieldPersEmail.getText();
        
        String currStreet = fieldCurrStreet.getText();
        String currApt = fieldCurrApt.getText();
        String currCity = fieldCurrCity.getText();
        long currZip = Long.valueOf(fieldCurrZip.getText());
        
        String permStreet = fieldPermStreet.getText();
        String permApt = fieldPermApt.getText();
        String permCity = fieldPermCity.getText();
        long permZip = Long.valueOf(fieldPermZip.getText());
        
        
        
        this.person.setNuid(nuid);
        this.person.setFirstName(firstName);
        this.person.setLastName(lastName);
        this.person.setClgName(clgName);
        
        Contact oContact = this.person.getoContact();
        oContact.setPhone(oPhone);
        oContact.setEmail(oEmail);
        
        Contact pContact = this.person.getpContact();
        pContact.setPhone(pPhone);
        pContact.setEmail(pEmail);
        
        Address currAddress = this.person.getCurrAddress();
        currAddress.setStreet(currStreet);
        currAddress.setApt(currApt);
        currAddress.setCity(currCity);
        currAddress.setZip(currZip);
        
        Address permAddress = this.person.getPermAddress();
        permAddress.setStreet(permStreet);
        permAddress.setApt(permApt);
        permAddress.setCity(permCity);
        permAddress.setZip(permZip);
        
        display();
    }//GEN-LAST:event_saveBtnActionPerformed

